// ____Arithmetic operators____

// gumarum +
console.log(8 + 17);

// hanum -
console.log("gdawhj" - 9);

// bazm *
console.log("gdhjwa" * 9);

// bajanum /
console.log(8 / 4);

// power **
console.log(2 ** 3);

// mnacordov bajanum
console.log(87 % 10);

//  __assignment operators__ =, +=, -=, *=, /=, %=, **=
let age = 10;
age += 2; // age = age + 2
console.log(age);

// __comparison__ ==, ===, !=, !==, >, <, >=, <=
let a = 18 === 8;
console.log(a);

// String operators +
console.log("I was born on November " + 13);
console.log(4 + (4 + "7"));
